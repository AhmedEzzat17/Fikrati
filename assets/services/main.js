window.addEventListener("load", () => {   //loading
  const loader = document.getElementById("loader");

  setTimeout(() => {
    loader.classList.add("bg-hidden");
  }, 1700);

  if (typeof initFadeInAnimations === "function") {
    setTimeout(() => {
      initFadeInAnimations();
    }, 2200);
  }
});

